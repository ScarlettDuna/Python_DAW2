# listas inmutables, guardan datos, se pueden repetir, de distinto tipo, pero inmutables
# Formas de crear una tupla
tupla1 = ()
print(tupla1)
tupla2 = ("a", 1, True)
print(tupla2)
tupla3 = tuple()
print(tupla3)
tupla4 = tuple([1,2,3])
print(tupla4)
tupla5 = 1,2,3
print(tupla5)
print(type(tupla5))

notupla6 = ("nombre")
print(notupla6)
print(type(notupla6))

tupla7 = ("nombre", )
print(tupla7)